"""
    Arthimetic operator
    An arthimetic operation is used to perform some operators and operands.
  The operators are  +'-,*,/,//,%,**
  Note:input()is a function which takes inputs from user.
  """
a= int(input("enter a number:"))
b= int(input("enter a number:"))
print("The Addition",a+b)
print("The Subtraction",a-b)
print("The Multiplication",a*b)
print("The Division",a/b)#Which returns the quotient.
print("The Floor Division",a//b)#Which returns the integral part of the quotient.
print("The Modular Division",a%b)#Which returns the remainder part before the decimal.
print("The Power",a**b)#Which returns the power.
        
        
        
        
        
        
        
        
        
        
        
        
  