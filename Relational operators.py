#RELATIONAL OPERTAORS
"""Relational operators are used to compare the values and retuen the boolean.They are<,>,<=,>=,==,!=
"""
a=int(input("Enter a number:"))
b=int(input("Enter a number:"))
print("The Greater value:",a>b)
print("The Lower value",a<b)
print("The Greater than or equals to value",a>=b)
print("The Lesser than or equals to value",a<=b)
print("The Equals to value",a==b)
print("The not equals to value",a!=b)
